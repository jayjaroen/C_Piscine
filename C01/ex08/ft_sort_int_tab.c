void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	swap;
	int	tmp;

	swap = -1;
	while (swap != 0)
	{
		i = 0;
		swap = 0;
		while (i < size - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				tmp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = tmp;
				swap++;
			}
			i++;
		}
	}
}
