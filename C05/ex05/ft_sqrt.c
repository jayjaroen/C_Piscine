int	ft_sqrt(int nb)
{
	int	sqr;

	sqr = 1;
	if (nb <= 0 || nb > 2147483647)
		return (0);
	while (sqr * sqr <= nb)
	{
		if (sqr * sqr == nb)
			return (sqr);
		sqr++;
	}
	return (0);
}
