int	ft_is_prime(int nb)
{
	int	i;
	int	prime;

	i = 2;
	prime = 0;
	if (nb > 2147483647)
		return (0);
	while (nb % 2 != 0 && i < nb)
	{
		if (nb % i != 0)
		{
			prime = 1;
			return (1);
		}
		i++;
	}
	return (0);
}
